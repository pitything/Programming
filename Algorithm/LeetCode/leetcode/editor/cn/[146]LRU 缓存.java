class LRUCache146{
    public static void main(String[] args) {
        LRUCache146 lruCache = new LRUCache146(2);
        lruCache.put(2, 2);
        lruCache.put(2, 3);
        lruCache.put(2, 4);
        System.out.println(lruCache.get(2));

    }

     class Node{
         int key;
         int value;
         Node pre;
         Node next;
         Node(){}
         Node(int key, int value){
             this.key = key;
             this.value = value;
         }
     }

     Map<Integer, Node> map = new HashMap<>();
     int size;
     int capacity;
     Node head;
     Node tail;

    public LRUCache146(int capacity){
        this.size = 0;
        this.capacity = capacity;
        head = new Node();
        tail = new Node();
        head.next = tail;
        tail.pre = head;
    }

    public int get(int key){
        Node node = map.get(key);
        if(node == null) return -1;
        removeNode(node);
        moveNode2Head(node);
        return node.value;
    }

    public void put(int key, int value){
        Node node = map.get(key);
        if(node != null){
            node.value = value;
            // map.put(key, node);
            removeNode(node);
            moveNode2Head(node);
        }else{
            node = new Node(key, value);
            map.put(key, node);
            moveNode2Head(node);
            size++;
            if(size > capacity){
                map.remove(tail.pre.key);
                removeNode(tail.pre);
                size--;
            }
        }
    }

    public void removeNode(Node node){
        node.pre.next = node.next;
        node.next.pre = node.pre;
    }

    public void moveNode2Head(Node node){
        head.next.pre = node;
        node.next = head.next;
        head.next = node;
        node.pre = head;
    }
}


